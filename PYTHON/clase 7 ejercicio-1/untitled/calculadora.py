class operaciones:

## DEFINIMOS LOS VALORES
    def valores(self, valor1, valor2):
        self.valor1=valor1
        self.valor2=valor2

## REALIZAMOS LA OPERACION SUMA
    def sumar(self):
        print(self.valor1," + ",self.valor2," = ", self.valor1 + self.valor2)
        return


## REALIZAMOS LA OPERACION RESTA
    def restar(self):
        print(self.valor1," - ",self.valor2," = ", self.valor1 - self.valor2 )
        return


## REALIZAMOS LA OPERACION DE MULTIPLICAR
    def multiplicar(self):
        print(self.valor1," * ",self.valor2," = ", self.valor1 * self.valor2)
        return


## REALIZAMOS LA OPERACION DE DIVIDIR
    def dividir (self):
        print(self.valor1," / ",self.valor2," = ", self.valor1 / self.valor2)
        return




